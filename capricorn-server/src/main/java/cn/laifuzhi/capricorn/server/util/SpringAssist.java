@Component
public final class SpringAssist implements ApplicationContextAware {
    private static ApplicationContext context;

    @Override
    public void setApplicationContext(ApplicationContext applicationContext) throws BeansException {
        context = applicationContext;
    }

    public static @Nonnull <T> T getBean(@Nonnull Class<T> clazzName) {
        return Objects.requireNonNull(context.getBean(clazzName));
    }

    public static @Nonnull <T> T getBean(@Nonnull String name, @Nonnull Class<T> clazzName) {
        return Objects.requireNonNull(context.getBean(name, clazzName));
    }
}
